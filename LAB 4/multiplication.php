<?php

// Define the function
function multiplication($num1, $num2)
{
    $result = $num1 * $num2;
    return $result;
}

// Call the function
$num1 = 5; // Replace with your first number
$num2 = 8; // Replace with your second number

$product = multiplication($num1, $num2);

// Display the result
echo "The multiplication of $num1 and $num2 is: $product";
